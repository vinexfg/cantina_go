export default function Navegacao() {
  const location = useLocation();
  const navigate = useNavigate();
  const tipo = localStorage.getItem('tipo');
  const { theme, toggle } = useTheme();

  const itens = tipo === 'cantina'
    ? [
        { path: '/vendedor',  icon: <IconGestao />,   label: 'Gestão'   },
        { path: '/reservas',  icon: <IconReservas />, label: 'Reservas' },
        { path: '/historico', icon: <IconHistorico />,label: 'Histórico'},
      ]
    : [
        { path: '/menu',         icon: <IconMenu />,    label: 'Menu'    },
        { path: '/meus-pedidos', icon: <IconPedidos />, label: 'Pedidos' },
        { path: '/perfil',       icon: <IconPerfil />,  label: 'Perfil'  },
      ];

  return (
    <>
      <button className={styles.floatingTheme} onClick={toggle} title="Alternar tema">
        {theme === 'light' ? <IconMoon /> : <IconSun />}
      </button>
      <nav className={styles.bottomNav}>
        {itens.map((item) => (
          <div
            key={item.path}
            className={`${styles.navItem} ${location.pathname === item.path ? styles.active : ''}`}
            onClick={() => navigate(item.path)}
          >
            <span className={styles.navIcon}>{item.icon}</span>
            <span className={styles.navText}>{item.label}</span>
          </div>
        ))}
      </nav>
    </>
  );
}
